import struct

from utils import get_resource_path


class CrcCaculator:

    __MASK1 = 0x9255AE41
    __MASK2 = 0xEFCFBFEA
    with open(get_resource_path("resource/crc_table.bin"), "rb") as f:
        __CRC_TABLE = struct.unpack('<256H', f.read())

    def calc(self, data: bytes) -> tuple[int, int]:
        crc = 0xFFFF
        for byte in data:
            crc = (crc >> 8) ^ CrcCaculator.__CRC_TABLE[(crc & 0xFF) ^ byte]
        crc ^= 0xFFFF
        left = crc & CrcCaculator.__MASK1 | CrcCaculator.__MASK2 & ~CrcCaculator.__MASK1
        right = crc & ~CrcCaculator.__MASK1 | CrcCaculator.__MASK2 & CrcCaculator.__MASK1
        return (left, right)


